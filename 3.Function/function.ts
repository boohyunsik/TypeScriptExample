/*
    반환값이 없는 경우는, 반환 형태를 void로 지정하던지, 비워둘 수 있습니다.
    아래 함수들은 단순히 Hello와 Hi를 출력하는 함수입니다.
*/

function printHello() {
    console.log("Hello")
}

// Hello가 출력될 것입니다.
printHello()

function printHi(): void {
    console.log("Hi")
}

// Hi가 출력될 것입니다.
printHi()